def phep_tinh_co_ban(a, b):
    print(f"{a} + {b} = {a + b}")
    print(f"{a} - {b} = {a - b}")
    print(f"{a} * {b} = {a * b}")
    if b != 0:
        print(f"{a} / {b} = {a / b}")
    else:
        print("Không thể thực hiện phép chia cho 0.")

def tinh_luy_thua_co_tham_so(x, y):
    return x ** y 
